#include "game_mng.hh"

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

GameMng::GameMng()
  : InputListener(15, "GameMng"),
    MInGameMng(NULL),
    MOutGameMng(NULL),
    MGameContext(NULL)
{
  MOutGameMng = new OutGameMng();
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

GameMng::~GameMng()
{
  delete MOutGameMng;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

void GameMng::Update()
{
  MOutGameMng->Update();
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

bool GameMng::OnKeyboardEvent(const KeyboardEvent& parEvt)
{
  if (parEvt.GetVirtualKey() == SDLK_ESCAPE)
  {
    StateHandler::get()->ChangeState(STATE_QUIT, "");
    return true;
  }

  return false;
}

