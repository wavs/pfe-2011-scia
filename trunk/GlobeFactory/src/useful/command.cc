#include "command.hh"
#include "executor.hh"
#include "log.hh"

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

Command::Command(const std::string& parName)
  : MName(parName)
{
  LOG_ASSERT_WITH_DESC(!MName.empty() && MName.size() <= 10,
     "Command name must be smaller than 10 chars (cf: " + MName + ")");

  Executor::get()->RegisterCommand(this);
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

Command::~Command()
{
  Executor::get()->UnregisterCommand(this);
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

void Command::Execute()
{
  LOG_ERROR("Command", MName + "(void) is not defined.");
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

void Command::Execute(bool)
{
  LOG_ERROR("Command", MName + "(bool) is not defined.");
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

void Command::Execute(float)
{
  LOG_ERROR("Command", MName + "(float) is not defined.");
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

void Command::Execute(const std::string&)
{
  LOG_ERROR("Command", MName + "(string) is not defined.");
}

